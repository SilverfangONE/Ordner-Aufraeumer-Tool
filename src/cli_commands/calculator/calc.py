"""
provides arithmetical operation functions
"""


def add(a: int, b: int):
    """ add a to b"""
    return a + b


def sub(a: int, b: int):
    """ subtract a from b"""
    return a - b


def multi(a: int, b: int):
    """ multiply a by b"""
    return a * b


def divide(a: int, b: int):
    """ divide a by b"""
    return a / b
